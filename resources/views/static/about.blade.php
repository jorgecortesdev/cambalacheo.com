<h4>&iquest;Qué es cambalacheo.com?</h4>
<p>Desarrollado y administrado por un experto en el desarrollo de applicaciones web. Esta página está diseñada para facilitar
el intercambio de artículos o servicios entre sus usuarios sobre una plataforma gratuita.</p>

<p>Nace de la idea de tener una alternativa a los sitios de venta ofreciendo un accesso rápido a lo que los otros usuarios
desean intercambiar.</p>

<p>La idea del intercambio va más allá del valor monetario de lo que se desea cambiar, siempre debes tener presente mientras
se realiza una oferta, cuanto te sirve o deseas lo que la otra persona tiene para ofrecer, esto significa por lo tanto,
<strong>cuanto vale para ti y no para el mercado</strong>.</p>

<p>Recuerda que no existe malos intercambios cuando obtienes lo que deseas sin gastar un solo peso.</p>

<h5>&iquest;Por qué intercambiar cosas?</h5>
<p>Es simple, muchas veces creemos que siempre ocupamos dinero para lograr algo, pero no debemos olvidar que en nuestro alrededor
siempre hay cosas de las cuales podemos echar mano para alcanzar lo que queremos, así que en resumidas cuentas:</p>
<ul>
    <li>Para obtener algo que deseamos.</li>
    <li>Para deshacernos de cosas que ya no ocupamos por algo que si ocupamos.</li>
    <li>Para obtener algún servicio sin gastar.</li>
    <li>Para adquirir conocimientos.</li>
</ul>

<h5>Las reglas del juego</h5>
<ul>
    <li>Ser honesto.</li>
    <li>No omitir ningún dato sobre el artículo o servicio que estas publicando.</li>
    <li>Cumple con tu palabra en todo momento.</li>
    <li>Promueve los intercambios y el reuso con tus amigos.</li>
</ul>

<h5>Ejemplo de intercambios</h5>
<ul>
    <li>Una laptop por un seminario de negocios con duración de 1 mes.</li>
    <li>Una subscripción anual en una academia de danza por un paquete de publicidad en radio.</li>
    <li>Una guitarra por un celular.</li>
    <li>Asesoría por literaturas diversas.</li>
</ul>

